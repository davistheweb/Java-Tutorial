public class AreaOfCircle {
    public static void main(String[] args) {
        int CircleRadius = 13;
        double Pi = 3.143;
        double Area = Pi * CircleRadius * CircleRadius;
        System.out.println("The area of the circle is: " + Area);
    }
}
